#include "ft.h"

int	main(int argc, char **argv)
{
	if (argc != 4)
		return (0);
	if (ft_sign(argv[2]) == 0)
		return (write(1, "0\n", 2));
	else
	{
		ft_makeop(argv[1], argv[2], argv[3]);
		write(1, "\n", 1);
	}
	return (0);
}
